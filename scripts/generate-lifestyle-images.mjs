const offeringFiles = {
  "Event Security": ["images/lifestyle-concierge/items/event-security.png"],
}

const excludedImages = new Set([
  "/images/aviation/airabulance/PHOTO-2026-07-10-20-24-56.jpg",
  "/images/aviation/airabulance/air-ambulance-jet.jpg",
  "/images/aviation/helicopter/heli-burj-al-arab.jpg",
  "/images/marine/yatchchatter/yacht-cruise.jpg",
  "/images/marine/superyatch/yacht-topdown.jpg",
])

const preferredFirstImage = {
  "Air Ambulance": "/images/aviation/airabulance/PHOTO-2026-07-12-11-27-55.jpg",
}

function collectImages(directory) {
  const absoluteDirectory = join(publicRoot, directory)
  return readdirSync(absoluteDirectory)
    .flatMap((name) => {
      const path = join(absoluteDirectory, name)
      return statSync(path).isDirectory() ? collectImages(join(directory, name)) : [path]
    })
    .filter((path) => imageExtensions.has(extname(path).toLowerCase()))
    .map((path) => `/${relative(publicRoot, path).split(sep).join("/")}`)
}

const catalog = {}
for (const [title, folders] of Object.entries(offeringFolders)) {
  catalog[title] = folders.flatMap(collectImages).filter((path) => !excludedImages.has(path))
}
for (const [title, files] of Object.entries(offeringFiles)) {
  catalog[title] = files.map((path) => `/${path}`)
}

for (const [title, images] of Object.entries(catalog)) {
  images.sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))
  const preferred = preferredFirstImage[title]
  if (preferred && images.includes(preferred)) {
    catalog[title] = [preferred, ...images.filter((image) => image !== preferred)]
  }
}

const source = `/** Generated by scripts/generate-lifestyle-images.mjs. Do not edit manually. */\nexport const lifestyleItemImageSets: Record<string, readonly string[]> = ${JSON.stringify(catalog, null, 2)}\n`
writeFileSync(outputFile, source)
console.log(`Generated ${outputFile} with ${Object.values(catalog).flat().length} assigned images.`)
